whats_the_weather = "sunny"

case whats_the_weather
when 'sunny'
	puts "Its another sunny day in California"
when 'rainy'
	puts "Bring an umbrella"
when 'cloudy'
	puts "Grab a light jacket"
when 'foggy'
	puts "Please drive safe out there"
else 
	puts "The weather is out of control- I don't know what to tell you!"
end